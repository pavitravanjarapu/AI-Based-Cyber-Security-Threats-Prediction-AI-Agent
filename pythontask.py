"""*Create a Python class BankAccount that allows users to deposit, withdraw, and check balance. Ensure balance doesn‚Äôt go below zero.*"""

class BankAccount:
    def __init__(self, owner, balance=0):
        self.owner = owner
        self.balance = balance

    def deposit(self, amount):
        self.balance += amount
        print(f"Deposited ‚Çπ{amount}. Current Balance: ‚Çπ{self.balance}")

    def withdraw(self, amount):
        if amount > self.balance:
            print("Insufficient balance!")
        else:
            self.balance -= amount
            print(f"Withdrawn ‚Çπ{amount}. Remaining Balance: ‚Çπ{self.balance}")

# Example
acc1 = BankAccount("Pavitra", 5000)
acc1.deposit(2000)
acc1.withdraw(1500)
acc1.withdraw(6000)

